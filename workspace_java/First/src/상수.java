import java.util.PrimitiveIterator;

public class 상수 {
  public static void main(String[] args) {
    // 상수 : 변하지 않는 값

    // 변수는 값으 ㄹ지속적으로 변경할 수 있음
    int num = 10;
    num = 20;

    // 자료형 앞에 final 키워드를 추가하면 상수가 됨.(실수, 정수, 문자열등 자료형 에 다 가능)
    // 상수는 값을 변경할수 없음.
    // 상수는 통상적으로 대문자로 명명
    final int NUM2 = 10;
    //NUM2 = 20; 상수는 값 변경 시 오류 발생!



  }
}
